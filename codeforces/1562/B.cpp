bool checkSubseq(str s, str s1) {
    //check s1 is a subseq of s
    ll n = sz(s1);
    ll pos = 0;
    fo(i, sz(s)) {
        if (s[i] == s1[pos]) {
            pos++;
        }
    }
    if (pos == n) return true;
    return false;
}


using ll = int64_t;
#define all(x) (x).begin(), (x).end()
const ll MOD = 998244353;

ll power(ll x, ll y, ll p = MOD) {
    ll res = 1;
    x %= p;
    if (x == 0) return 0;
    while (y > 0) {
        if (y & 1)
            res = (res * x) % p;
        y = y >> 1;
        x = (x * x) % p;
    }
    return res % p;
}

void Solution() {
    // m^k^n %MOD
    ll n, k, m;
    cin >> n >> k >> m;
    ll k_n = power(k, n, MOD - 1);
    ll m_k_n = power(m, k_n, MOD);
    cout << m_k_n << '\n';
}

